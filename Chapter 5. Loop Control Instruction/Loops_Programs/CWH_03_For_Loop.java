package Loops_Programs;

public class CWH_03_For_Loop {
    public static void main(String[] args) {
        
        // Incrementing For-Loop
        for (int x = 1; x<=10 ;x++) {
            System.out.println(x);
        }
        // Decrementing For-Loop
        for (int i = 10; i>=0 ;i--) {
            System.out.println(i);    
        }
    }
}
