public class CWH_02_Literals 
{
    public static void main(String []args)
    {
        long big = 33367L; // Integer Literal
        byte age = 22; // Integer literal
        char ch = 'B'; // Character literal
        float f = 5.6f; // Floating literal
        double d = 3.5; // Double literal
        String s = "Himank"; // String literal
        boolean a = true; // Boolean literal
        System.out.println("Age is = "+age);
        System.out.println(ch);
        System.out.println("Float number = "+f);
        System.out.println("Double number = "+d);
        System.out.println(s);
        System.out.println(a);
        System.out.println(big);

    }       
}